def get_text(lang: str, *keys: str, **kwargs) -> str:
    """
    Get translated text for the given language.

    Usage:
        get_text("uz", "welcome", "title", name="User")
        get_text("ru", "general", "yes")
        get_text("en", "bid", "sent", bid_id=5)

    Falls back to 'uz' if key not found in requested language.
    Returns the last key as fallback if nothing found.
    """
    lang = lang if lang in TEXTS else "uz"

    # Navigate nested dict
    d = TEXTS[lang]
    for key in keys:
        if isinstance(d, dict) and key in d:
            d = d[key]
        else:
            # Fallback to Uzbek
            d = TEXTS["uz"]
            for k in keys:
                if isinstance(d, dict) and k in d:
                    d = d[k]
                else:
                    return keys[-1]
            break

    if isinstance(d, str):
        if kwargs:
            try:
                d = d.format(**kwargs)
            except (KeyError, ValueError):
                pass
        return d

    return keys[-1]


async def get_user_lang(update, context) -> str:
    """
    Get user's preferred language.
    Checks context.user_data first, then falls back to DB.
    """
    # Check context cache first
    if context and context.user_data and "lang" in context.user_data:
        return context.user_data["lang"]

    # Try DB
    try:
        from api.database import SessionLocal
        from api.database.models import User
        user_id = update.effective_user.id
        db = SessionLocal()
        user = db.query(User).filter(User.id == user_id).first()
        db.close()
        lang = user.lang if user and user.lang else "uz"
        # Cache in context
        if context and context.user_data:
            context.user_data["lang"] = lang
        return lang
    except Exception:
        return "uz"


def get_category_name(cat: str, lang: str = "uz") -> str:
    """Get translated category name"""
    return get_text(lang, "category", cat, default=cat.capitalize())


def get_role_name(role: str, lang: str = "uz") -> str:
    """Get translated role name"""
    return get_text(lang, "role", role, default=role)
